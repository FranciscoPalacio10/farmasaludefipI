    private Boolean filtrarJCombo(JComboBox a) {
        boolean seleccionado;
        seleccionado = !a.getSelectedItem().toString().equals("Seleccionar");

        return seleccionado;
    }

    private void llenarTabla() {
        modeloInventario = new jtLoteInventario(manager);
        modeloInventario.updateTable();
        tableInventario.setModel(modeloInventario);
        pintar = new pintarCeldas("fechaVTO");
        tableInventario.getColumn("Fecha VTO").setCellRenderer(pintar);
        tableInventario.getTableHeader().setReorderingAllowed(false);
    }

    private Boolean filtrarDate(JDateChooser a) {
        boolean seleccionado;
        seleccionado = a.getDate() != null;
        return seleccionado;
    }

    private void listenerFechaDesde() {
        dateDesde.getDateEditor().addPropertyChangeListener((PropertyChangeEvent e) -> {
            if ("date".equals(e.getPropertyName())) {
                if (filtrarDate(dateDesde) == true) {
                    fechaDesde = RowFilter.dateFilter(RowFilter.ComparisonType.AFTER, dateDesde.getDate(), 2);
                    lisTfiltros.add(fechaDesde);
                    habiltiarBotones();
                } else {
                    deshabilitarBotones();
                }
            }
        });

    }

    private void listenerFechaHasta() {
        dateHasta.getDateEditor().addPropertyChangeListener((PropertyChangeEvent e) -> {
            if ("date".equals(e.getPropertyName())) {
                if (filtrarDate(dateHasta) == true) {

                    fechaHasta = RowFilter.dateFilter(RowFilter.ComparisonType.BEFORE, dateHasta.getDate(), 2);
                    lisTfiltros.add(fechaHasta);
                    habiltiarBotones();
                } else {
                    deshabilitarBotones();
                }

            }
        });
    }

