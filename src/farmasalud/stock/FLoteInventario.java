    private void btnLimpiarFiltrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimpiarFiltrosActionPerformed
        andfiltros.removeAll(andfiltros);
        lisTfiltros.removeAll(lisTfiltros);
        fechaHasta = null;
        fechaDesde = null;
        nroLote = null;
        jComboLote.setSelectedIndex(0);
        deshabilitarBotones();
        dateDesde.setCalendar(null);
        dateHasta.setCalendar(null);
        llenarTabla();
        sorter = new TableRowSorter(tableInventario.getModel());
        tableInventario.setRowSorter(sorter);
    }//GEN-LAST:event_btnLimpiarFiltrosActionPerformed

    private void jComboLoteItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboLoteItemStateChanged
        if (filtrarJCombo(jComboLote)) {
            habiltiarBotones();
            nroLote = RowFilter.regexFilter(jComboLote.getSelectedItem().toString(), 0);
            lisTfiltros.add(nroLote);
        } else {
            deshabilitarBotones();
        }

    }//GEN-LAST:event_jComboLoteItemStateChanged

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        lisTfiltros.forEach((RowFilter<Object, Object> a) -> {
            if (a != null) {
                andfiltros.add(a);
            }
        });
        nuevoFilter = RowFilter.andFilter(andfiltros);
        sorter.setRowFilter(nuevoFilter);

    }//GEN-LAST:event_btnBuscarActionPerformed
    private void habiltiarBotones() {
        btnBuscar.setEnabled(true);
        btnLimpiarFiltros.setEnabled(true);
    }

    private void deshabilitarBotones() {
        btnBuscar.setEnabled(false);
        btnLimpiarFiltros.setEnabled(false);
    }

