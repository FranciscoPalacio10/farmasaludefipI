package farmasalud.atributosclases;

/**
 *
 * @author Francisco Palacio
 */
public class AProducto {
    int idProducto,precioUnitario,puntoDePedido,temperaturaConservacion,idVademecum,idNoFamaceutico;

    public AProducto() {
    }

    public int getIdProducto() {
        return idProducto;
    }

    public void setIdProducto(int idProducto) {
        this.idProducto = idProducto;
    }

    public int getPrecioUnitario() {
        return precioUnitario;
    }

    public void setPrecioUnitario(int precioUnitario) {
        this.precioUnitario = precioUnitario;
    }

    public int getPuntoDePedido() {
        return puntoDePedido;
    }

    public void setPuntoDePedido(int puntoDePedido) {
        this.puntoDePedido = puntoDePedido;
    }

    public int getTemperaturaConservacion() {
        return temperaturaConservacion;
    }

    public void setTemperaturaConservacion(int temperaturaConservacion) {
        this.temperaturaConservacion = temperaturaConservacion;
    }

    public int getIdVademecum() {
        return idVademecum;
    }

    public void setIdVademecum(int idVademecum) {
        this.idVademecum = idVademecum;
    }

    public int getIdNoFamaceutico() {
        return idNoFamaceutico;
    }

    public void setIdNoFamaceutico(int idNoFamaceutico) {
        this.idNoFamaceutico = idNoFamaceutico;
    }
    
    
    
}
