public class AUbicacionFisica {
    private int idUbicacionFisica,idEstante,idPasillo,idHeladera,idProducto,cantidad;
    private String idSucursal,idMedida;

    public AUbicacionFisica() {
    }

    public int getIdUbicacionFisica() {
        return idUbicacionFisica;
    }

    public void setIdUbicacionFisica(int idUbicacionFisica) {
        this.idUbicacionFisica = idUbicacionFisica;
    }

    public int getIdEstante() {
        return idEstante;
    }

    public void setIdEstante(int idEstante) {
        this.idEstante = idEstante;
    }

    public int getIdPasillo() {
        return idPasillo;
    }

    public void setIdPasillo(int idPasillo) {
        this.idPasillo = idPasillo;
    }

    public int getIdHeladera() {
        return idHeladera;
    }

    public void setIdHeladera(int idHeladera) {
        this.idHeladera = idHeladera;
    }

    public int getIdProducto() {
        return idProducto;
    }

    public void setIdProducto(int idProducto) {
        this.idProducto = idProducto;
    }

    public int getCantidad() {
        return cantidad;
    }

    public void setCantidad(int cantidad) {
        this.cantidad = cantidad;
    }

    public String getIdSucursal() {
        return idSucursal;
    }

    public void setIdSucursal(String idSucursal) {
        this.idSucursal = idSucursal;
    }

    public String getIdMedida() {
        return idMedida;
    }

    public void setIdMedida(String idMedida) {
        this.idMedida = idMedida;
    }
    
    
    
}
