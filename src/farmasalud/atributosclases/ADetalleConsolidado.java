package farmasalud.atributosclases;

/**
 *
 * @author Francisco Palacio
 */
public class ADetalleConsolidado {
    private int idProducto,total,nombreProducto,Medida;

    public ADetalleConsolidado() {
    }

    public int getIdProducto() {
        return idProducto;
    }

    public void setIdProducto(int idProducto) {
        this.idProducto = idProducto;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public int getNombreProducto() {
        return nombreProducto;
    }

    public void setNombreProducto(int nombreProducto) {
        this.nombreProducto = nombreProducto;
    }

    public int getMedida() {
        return Medida;
    }

    public void setMedida(int Medida) {
        this.Medida = Medida;
    }
    
    
    
    
}
